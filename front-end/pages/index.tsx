import type { GetStaticProps, NextPage } from 'next'
import Head from 'next/head'
import { useRecoilState } from 'recoil'
import InputBox from '../components/inputBox'
import PokectList from '../components/poketList'
import { searchValueAtom } from '../Util/recoil/state'
import { PokectmonListType } from '../types';
import { gql, useQuery } from '@apollo/client';
import { GET_POKETMONS } from '../gql'


interface getPoketmonType {
  findAll :PokectmonListType[]
}

const Home: NextPage<{data: getPoketmonType}> = ({data}) => {
  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <InputBox />
      <PokectList data={data}  />
    </>
  )
}

export const getStaticProps:GetStaticProps = () => {
  const searchValue = useRecoilState(searchValueAtom);
  
  const { data:POKETLIST , loading, error } = useQuery<getPoketmonType>(
      GET_POKETMONS,
      searchValue 
      ? {
          variables: {
          isAdult: false,
          type: 'POKECT',
          search: searchValue,
          } 
      }   
      : {
          variables: {
          isAdult: false,
          type: 'POKECT',
          }
      } 
  );
  if (loading) {return <h2>Loading...</h2>}
  if (error) {return <h1>에러 발생</h1>}
  
  return {
      props: {
          data:POKETLIST
      }
  }
}

export default Home
